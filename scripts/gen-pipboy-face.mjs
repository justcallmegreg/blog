// scripts/gen-pipboy-face.mjs
// One-time: read the Vault Boy source image, key out the near-white background
// (bright + low-chroma pixels, feathered at the edge), and write the green face
// as a base64 data URI TS const for the QR centre emblem.
// Usage: node scripts/gen-pipboy-face.mjs [srcImage] [outTs]
import sharp from 'sharp';
import { writeFileSync } from 'node:fs';

const src = process.argv[2] || `${process.env.HOME}/Desktop/pipboy.png`;
const out = process.argv[3] || 'src/lib/qr/pipboy-face.ts';

const { data, info } = await sharp(src).ensureAlpha().raw().toBuffer({ resolveWithObject: true });
const { width, height, channels } = info;
for (let i = 0; i < data.length; i += channels) {
  const r = data[i], g = data[i + 1], b = data[i + 2];
  const mx = Math.max(r, g, b), mn = Math.min(r, g, b), chroma = mx - mn;
  if (mn >= 235 && chroma <= 22) {
    data[i + 3] = 0; // near-white bg -> transparent
  } else if (mn >= 205 && chroma <= 40) {
    const a = Math.round(255 * Math.min(1, (235 - mn) / 30 + chroma / 60)); // feather the halo
    data[i + 3] = Math.min(data[i + 3], Math.max(0, a));
  }
}
const png = await sharp(data, { raw: { width, height, channels } }).png({ compressionLevel: 9 }).toBuffer();
const b64 = png.toString('base64');
writeFileSync(
  out,
  `// AUTO-GENERATED by scripts/gen-pipboy-face.mjs — do not edit by hand.\n` +
    `// Background-removed Vault Boy face, embedded as a data URI for the QR centre emblem.\n` +
    `export const PIPBOY_FACE = 'data:image/png;base64,${b64}';\n`
);
console.log('wrote', out, `(${b64.length} b64 chars, source ${width}x${height})`);
